package com.example.excelsheet;

public class viewClass {

    private int first;
    private int second;
    private int third;
    private int  fourth;
    private int fifth;

    public  viewClass(int f, int s, int t, int fo, int fif) {
        first = f;
        second = s;
        third = t;
        fourth = fo;
        fifth = fif;
    }

    public int getFirst() {
        return first;
    }

    public int getSecond() {
        return second;
    }

    public int getThird() {
        return third;
    }

    public int getFourth() {
        return fourth;
    }

    public int getFifth() {
        return fifth;
    }
}
